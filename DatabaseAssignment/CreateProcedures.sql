use Archer

create procedure AddCustomer(
    @FirstName varchar(32),
    @LastName varchar(64),
    @Email varchar(64),
    @ID int output
)
as begin
    insert into 
        Customer
    values
        (@FirstName, @LastName, @Email)

    set @ID = SCOPE_IDENTITY();
end
go

create procedure AddProduct(
    @Name varchar(32),
    @Category varchar(32),
    @Price decimal(19, 4),
    @ID int output
)
as begin
    insert into
        Product
    values
        (@Name, @Category, @Price)

    set @ID = SCOPE_IDENTITY();
end
go

create procedure AddOrder(
    @CustomerId int,
    @ProductId int,
    @ID int output
)
as begin
    insert into
        [Order]
    values
        (@CustomerId, @ProductId, getdate())

    set @ID = SCOPE_IDENTITY();
end
go

select * from [Order]